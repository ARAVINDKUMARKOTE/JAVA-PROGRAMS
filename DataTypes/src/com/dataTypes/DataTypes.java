package com.dataTypes;

public class DataTypes {
	public static void main(String[] args) {
		byte a=10;//-128 to 127
		short b=11;// -32.768 to 32.767
		int c=12;//-2,147,483,648 to 2,147,483,647
		long d=13l;
		float f=13.09f;//6 to 7 decimal points
		double g=14.3456;//15 decimal points
		char h='A';//single character value
		boolean i=true;//true or false
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(i);
		
	}

}
