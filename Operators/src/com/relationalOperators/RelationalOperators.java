package com.relationalOperators;

public class RelationalOperators {
		public static void main(String[] args) {
			int a =10;
			int b=20;
			System.out.println("a + b = "+(a+b));
			System.out.println("a - b = "+(a-b));
			System.out.println("a * b = "+(a*b));
			System.out.println("a / b = "+(a/b));
			System.out.println("a % b = "+(a%b));
	}
}
	/*
	These are like workouts in gym,each one works on specific muscle group,
	just like each operator works acts on specific data types.
	+ -> Addition
	- -> Subtraction
	* -> multiplication.
	/ -> Division
	% -> Modulus
	 */
