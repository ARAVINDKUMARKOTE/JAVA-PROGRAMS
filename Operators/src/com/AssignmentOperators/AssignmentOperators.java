package com.AssignmentOperators;

public class AssignmentOperators {
	public static void main(String[] args) {
		int a=10;
		a+=5;// a is now 15
		System.out.println(a);
		a-=5;// a is now 10
		System.out.println(a);
		a*=5;// a is now 50
		System.out.println(a);
		a/=5;// a is now 10
		System.out.println(a);
		a%=5;// a is now 0
		System.out.println(a);
		
		
	}

}
